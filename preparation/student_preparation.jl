		* You **pay \$$(c)** for each unit that you stock.
		* You **get \$$(p)** for each unit that you sell to your customer.
		* Customer **demand is uncertain** and will be **between $l and $u** every day. 
		* Independent of the demand of the previous day, you **expect Œº = $(my_round(Œº))** and face uncertainty captured by a standard deviation of **œÉ = $(my_round(œÉ))**. The distribution is shown in the figure to the right).
